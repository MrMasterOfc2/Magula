// ==================== PUBLIC BOOKING CALENDAR ====================
let publicCurrentDate = new Date();

function renderPublicBookingCalendar() {
    const container = document.querySelector("#public-booking-calendar");
    if (!container) return;
    
    const year = publicCurrentDate.getFullYear();
    const month = publicCurrentDate.getMonth();
    
    const firstDayOfMonth = new Date(year, month, 1);
    const startWeekday = firstDayOfMonth.getDay();
    const daysInMonth = new Date(year, month + 1, 0).getDate();
    
    const monthNames = ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"];
    
    let html = `<div class="booking-calendar-nav" style="display: flex; justify-content: space-between; margin-bottom: 16px;">
                    <button id="public-cal-prev" class="btn btn-light" style="padding: 5px 12px;">&lt; Previous</button>
                    <strong>${monthNames[month]} ${year}</strong>
                    <button id="public-cal-next" class="btn btn-light" style="padding: 5px 12px;">Next &gt;</button>
                </div>`;
    html += `<div class="booking-calendar-weekdays" style="display: grid; grid-template-columns: repeat(7, 1fr); gap: 8px; margin-bottom: 8px;">`;
    const weekdays = ["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"];
    weekdays.forEach(day => {
        html += `<div style="font-weight: bold; text-align: center;">${day}</div>`;
    });
    html += `</div><div class="booking-calendar-grid" style="display: grid; grid-template-columns: repeat(7, 1fr); gap: 8px;">`;
    
    for (let i = 0; i < startWeekday; i++) {
        html += `<div class="booking-date-empty" style="min-height: 80px; background: rgba(255,255,255,0.2); border-radius: 12px;"></div>`;
    }
    
    const bookings = getBookings();
    const approvedBookings = bookings.filter(b => b.status === "approved");
    
    const bookingMap = {};
    approvedBookings.forEach(booking => {
        const dateKey = new Date(booking.startDate).toISOString().split('T')[0];
        if (!bookingMap[dateKey]) bookingMap[dateKey] = [];
        bookingMap[dateKey].push(booking);
    });
    
    for (let d = 1; d <= daysInMonth; d++) {
        const date = new Date(year, month, d);
        const dateKey = date.toISOString().split('T')[0];
        const bookingsOnDate = bookingMap[dateKey] || [];
        
        let badgeHtml = "";
        if (bookingsOnDate.length > 0) {
            badgeHtml = `<div style="margin-top: 5px;">
                            ${bookingsOnDate.map(b => `<span style="display: inline-block; background: var(--green); color: white; font-size: 0.65rem; padding: 2px 6px; border-radius: 10px; margin: 2px;" title="${b.name} - ${b.purpose}">${b.facility.substring(0, 12)}</span>`).join('')}
                         </div>`;
        }
        
        html += `<div class="booking-date ${bookingsOnDate.length > 0 ? 'has-booking' : ''}" style="min-height: 80px; border: 1px solid var(--line); border-radius: 12px; padding: 8px; background: var(--glass);">
                    <div class="booking-date-number" style="font-weight: bold;">${d}</div>
                    ${badgeHtml}
                </div>`;
    }
    html += `</div>`;
    container.innerHTML = html;
    
    const prevBtn = document.querySelector("#public-cal-prev");
    const nextBtn = document.querySelector("#public-cal-next");
    if (prevBtn) prevBtn.addEventListener("click", () => {
        publicCurrentDate.setMonth(publicCurrentDate.getMonth() - 1);
        renderPublicBookingCalendar();
    });
    if (nextBtn) nextBtn.addEventListener("click", () => {
        publicCurrentDate.setMonth(publicCurrentDate.getMonth() + 1);
        renderPublicBookingCalendar();
    });
}

// ==================== BOOKING FORM HANDLER (SINGLE DATE) ====================
function initBookingForm() {
  const bookingForm = document.querySelector("#public-booking-form");
  if (!bookingForm) return;
  
  bookingForm.addEventListener("submit", async (event) => {
    event.preventDefault();
    
    const name = document.querySelector("#booking-name")?.value.trim();
    const phone = document.querySelector("#booking-phone")?.value.trim();
    const email = document.querySelector("#booking-email")?.value.trim();
    const nic = document.querySelector("#booking-nic")?.value.trim();
    const facility = document.querySelector("#booking-facility")?.value;
    const purpose = document.querySelector("#booking-purpose")?.value.trim();
    const bookingDate = document.querySelector("#booking-date")?.value;
    const details = document.querySelector("#booking-details")?.value.trim();
    const messageDiv = document.querySelector("#booking-message");
    
    if (!name || !phone || !nic || !facility || !purpose || !bookingDate) {
      if (messageDiv) {
        const currentLang = localStorage.getItem("galewela-language") || "si";
        const t = translations[currentLang];
        messageDiv.textContent = t.bookingError || "Please fill in all required fields.";
        messageDiv.style.display = "block";
        messageDiv.style.background = "rgba(255,0,0,0.1)";
        messageDiv.style.color = "red";
        setTimeout(() => { messageDiv.style.display = "none"; }, 3000);
      }
      return;
    }
    
    const newBooking = {
      id: crypto.randomUUID ? crypto.randomUUID() : Date.now().toString(),
      name,
      phone,
      email: email || "",
      nic,
      facility,
      purpose,
      startDate: bookingDate,
      endDate: bookingDate,
      details: details || "",
      status: "pending",
      createdAt: new Date().toISOString()
    };

    const submitButton = bookingForm.querySelector("button[type='submit']");
    if (submitButton) {
      submitButton.disabled = true;
      submitButton.textContent = "Saving...";
    }

    try {
      await savePublicBooking(newBooking);
      bookingForm.reset();
      if (messageDiv) {
        const currentLang = localStorage.getItem("galewela-language") || "si";
        const t = translations[currentLang];
        messageDiv.textContent = t.bookingSuccess || "✓ Your booking request has been submitted successfully. Please wait for approval.";
        messageDiv.style.display = "block";
        messageDiv.style.background = "rgba(23,105,224,0.15)";
        messageDiv.style.color = "var(--green)";
        setTimeout(() => { messageDiv.style.display = "none"; }, 4000);
      }
    } catch (error) {
      console.error(error);
      if (messageDiv) {
        messageDiv.textContent = `Booking not saved. ${getFirebaseErrorText(error)}`;
        messageDiv.style.display = "block";
        messageDiv.style.background = "rgba(255,0,0,0.1)";
        messageDiv.style.color = "red";
      }
    } finally {
      if (submitButton) {
        submitButton.disabled = false;
        submitButton.innerHTML = '<i data-lucide="send"></i>Submit Booking Request';
        refreshIcons();
      }
    }
  });
}

