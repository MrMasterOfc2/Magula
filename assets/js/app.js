async function saveFirebaseChatMessage(message) {
  const sentAt = new Date().toISOString();
  const payload = {
    name: message.name.slice(0, 40),
    text: message.text.slice(0, 500),
    role: "public",
    sentAt,
    replies: [],
    source: "website-chat",
    createdAtMs: Date.now(),
    createdAtIso: sentAt
  };
  const realtimeDb = await getSiteRealtimeDb();
  if (realtimeDb) {
    const ref = realtimeDb.ref("galewela-public-chat").push();
    await ref.set({
      id: ref.key,
      ...payload,
      createdAt: firebase.database.ServerValue.TIMESTAMP
    });
    return;
  }
  const firestore = getSiteDb();
  if (!firestore) throw new Error("Firebase is not configured.");
  await firestore.collection("galewela-public-chat").add({
    ...payload,
    createdAt: firebase.firestore.FieldValue.serverTimestamp()
  });
}

