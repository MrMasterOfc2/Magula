function escapeHtml(str) {
  if (!str) return "";
  return str.replace(/[&<>]/g, function(m) {
    if (m === "&") return "&amp;";
    if (m === "<") return "&lt;";
    if (m === ">") return "&gt;";
    return m;
  });
}

function hasSiteFirebaseConfig(config) {
  return Boolean(config && config.apiKey && config.projectId && config.appId);
}

function getSiteDb() {
  if (siteDb) return siteDb;
  const config = window.GALEWELA_FIREBASE_CONFIG;
  if (!hasSiteFirebaseConfig(config) || !window.firebase || !window.firebase.initializeApp) return null;
  try {
    if (!firebase.apps.length) firebase.initializeApp(config);
    siteDb = firebase.firestore();
    return siteDb;
  } catch (error) {
    console.error(error);
    return null;
  }
}

function loadFirebaseDatabaseSdk() {
  if (window.firebase && window.firebase.database) return Promise.resolve();
  if (firebaseDatabaseReadyPromise) return firebaseDatabaseReadyPromise;
  firebaseDatabaseReadyPromise = new Promise((resolve, reject) => {
    const existing = document.querySelector('script[data-firebase-database="true"]');
    if (existing) {
      existing.addEventListener("load", resolve, { once: true });
      existing.addEventListener("error", reject, { once: true });
      return;
    }
    const script = document.createElement("script");
    script.src = "https://www.gstatic.com/firebasejs/10.12.5/firebase-database-compat.js";
    script.dataset.firebaseDatabase = "true";
    script.onload = resolve;
    script.onerror = () => reject(new Error("Firebase Realtime Database SDK not loaded."));
    document.head.append(script);
  });
  return firebaseDatabaseReadyPromise;
}

async function getSiteRealtimeDb() {
  if (siteRealtimeDb) return siteRealtimeDb;
  const config = window.GALEWELA_FIREBASE_CONFIG;
  if (!hasSiteFirebaseConfig(config) || !config.databaseURL || !window.firebase || !window.firebase.initializeApp) return null;
  try {
    if (!firebase.apps.length) firebase.initializeApp(config);
    await loadFirebaseDatabaseSdk();
    siteRealtimeDb = firebase.database();
    return siteRealtimeDb;
  } catch (error) {
    console.error(error);
    return null;
  }
}

function objectToArray(value) {
  if (Array.isArray(value)) return value;
  if (!value || typeof value !== "object") return [];
  return Object.entries(value)
    .filter(([, item]) => item && typeof item === "object")
    .map(([id, item]) => ({ id, ...item }));
}

function bookingsArrayToMap(bookings) {
  return bookings.reduce((map, booking) => {
    const id = booking.id || (window.crypto && crypto.randomUUID ? crypto.randomUUID() : String(Date.now() + Math.random()));
    map[id] = { ...booking, id };
    return map;
  }, {});
}

function getFirebaseErrorText(error) {
  const code = error && (error.code || error.message);
  if (!code) return "Firebase unavailable";
  if (String(code).includes("permission_denied")) return "Firebase permission denied. Check Realtime Database rules.";
  if (String(code).includes("PERMISSION_DENIED")) return "Firebase permission denied. Check Realtime Database rules.";
  return `Firebase error: ${code}`;
}

