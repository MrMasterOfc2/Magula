async function saveFirebaseContactMessage(form) {
  const name = form.querySelector("input[type='text']")?.value.trim() || "Website Visitor";
  const email = form.querySelector("input[type='email']")?.value.trim() || "";
  const text = form.querySelector("textarea")?.value.trim() || "";
  if (!text) return;
  const sentAt = new Date().toISOString();
  await saveRealtimeChatPayload({
    name: name.slice(0, 40),
    email: email.slice(0, 120),
    text: text.slice(0, 500),
    role: "public",
    replies: [],
    source: "contact-form",
    createdAtMs: Date.now(),
    createdAtIso: sentAt,
    sentAt
  });
}

async function bootFirebaseChat() {
  if (!chatStatus || !chatMessages) return;
  const realtimeDb = await getSiteRealtimeDb();
  if (realtimeDb) {
    saveMessage = saveFirebaseChatMessage;
    chatStatus.textContent = "Connected to Firebase";
    realtimeDb.ref("galewela-public-chat").limitToLast(50).on("value", (snapshot) => {
      const messages = sortChatMessages(objectToArray(snapshot.val())).slice(-50);
      renderMessages(messages);
      chatStatus.textContent = "Connected to Firebase";
    }, (error) => {
      console.error(error);
      chatStatus.textContent = "Firebase chat unavailable";
      renderMessages([]);
    });
    return;
  }
  const firestore = getSiteDb();
  if (!firestore) {
    saveMessage = saveFirebaseChatMessage;
    bootLocalChat("Firebase unavailable");
    return;
  }
  saveMessage = saveFirebaseChatMessage;
  chatStatus.textContent = "Connected to Firebase";
  firestore.collection("galewela-public-chat")
    .onSnapshot((snapshot) => {
      const messages = sortChatMessages(snapshot.docs.map((doc) => ({ id: doc.id, ...doc.data() }))).slice(-50);
      renderMessages(messages);
      chatStatus.textContent = "Connected to Firebase";
    }, (error) => {
      console.error(error);
      chatStatus.textContent = "Firebase chat unavailable";
      renderMessages([]);
    });
}

if (chatForm) {
  chatForm.addEventListener("submit", async (event) => {
    event.preventDefault();
    const nameInput = document.querySelector("#chat-name");
    const messageInput = document.querySelector("#chat-message");
    const name = nameInput.value.trim();
    const text = messageInput.value.trim();

    if (!name || !text) return;

    messageInput.value = "";
    messageInput.focus();
    if (chatStatus) chatStatus.textContent = "Saving message...";

    try {
      await saveMessage({ name, text });
      nameInput.value = name;
      if (chatStatus) chatStatus.textContent = saveMessage === saveFirebaseChatMessage ? "Connected to Firebase" : chatStatus.textContent;
    } catch (error) {
      console.error(error);
      messageInput.value = text;
      if (chatStatus) chatStatus.textContent = "Message not saved. Firebase unavailable";
    }
  });
}

