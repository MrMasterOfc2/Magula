function formatDate(value) {
  if (!value) return "";
  const date = value.toDate ? value.toDate() : new Date(value);
  if (Number.isNaN(date.getTime())) return "";
  return date.toLocaleString([], { dateStyle: "medium", timeStyle: "short" });
}

function getMessageTime(message) {
  return message.createdAtMs || message.sentAt || message.createdAt || message.createdAtIso;
}

function toChatTime(value) {
  if (!value) return 0;
  if (typeof value === "number") return value;
  if (value.toDate) return value.toDate().getTime();
  return Date.parse(value) || 0;
}

function sortChatMessages(messages, direction = "asc") {
  const multiplier = direction === "desc" ? -1 : 1;
  return [...messages].sort((a, b) => {
    const aTime = Number(a.createdAtMs) || toChatTime(a.sentAt) || toChatTime(a.createdAtIso) || toChatTime(a.createdAt);
    const bTime = Number(b.createdAtMs) || toChatTime(b.sentAt) || toChatTime(b.createdAtIso) || toChatTime(b.createdAt);
    return (aTime - bTime) * multiplier;
  });
}

function renderMessages(messages) {
  if (!chatMessages) return;
  chatMessages.innerHTML = "";
  messages.forEach((message) => {
    const item = document.createElement("article");
    item.className = "chat-message";
    if (message.role === "admin") item.classList.add("chat-message--admin");
    item.innerHTML = `
      <strong></strong>
      <p></p>
      <time></time>
      <div class="chat-replies"></div>
    `;
    item.querySelector("strong").textContent = message.name;
    item.querySelector("p").textContent = message.text;
    item.querySelector("time").textContent = formatDate(getMessageTime(message));
    const replies = Array.isArray(message.replies) ? message.replies : [];
    const repliesContainer = item.querySelector(".chat-replies");
    replies.forEach((reply) => {
      const replyItem = document.createElement("article");
      replyItem.className = "chat-admin-reply";
      replyItem.innerHTML = `
        <strong>Admin Reply</strong>
        <p></p>
        <time></time>
      `;
      replyItem.querySelector("p").textContent = reply.text || "";
      replyItem.querySelector("time").textContent = formatDate(reply.sentAt || reply.createdAt);
      repliesContainer.append(replyItem);
    });
    chatMessages.append(item);
  });
  chatMessages.scrollTop = chatMessages.scrollHeight;
}

