// ==================== FIREBASE SYNC ====================
async function bootSiteFirebaseSync() {
  const realtimeDb = await getSiteRealtimeDb();
  if (realtimeDb) {
    realtimeDb.ref("galewela-site/content").on("value", (snapshot) => {
      const data = snapshot.val() || {};
      delete data.updatedAt;
      adminContentState = {
        ...data,
        details: { ...defaultSiteDetails, ...(data.details || {}) },
        pageDetails: Array.isArray(data.pageDetails) ? data.pageDetails : []
      };
      refreshDynamicContent();
    }, console.error);

    realtimeDb.ref("galewela-bookings").on("value", (snapshot) => {
      bookingState = objectToArray(snapshot.val()).sort((a, b) => new Date(b.createdAt || 0) - new Date(a.createdAt || 0));
      if (document.querySelector("#public-booking-calendar")) {
        renderPublicBookingCalendar();
      }
    }, console.error);
    return;
  }

  const firestore = getSiteDb();
  if (!firestore) return;

  firestore.collection("galewela-site").doc("content").onSnapshot((doc) => {
    if (!doc.exists) return;
    const data = doc.data() || {};
    delete data.updatedAt;
    adminContentState = {
      ...data,
      details: { ...defaultSiteDetails, ...(data.details || {}) },
      pageDetails: Array.isArray(data.pageDetails) ? data.pageDetails : []
    };
    refreshDynamicContent();
  }, console.error);
  
  firestore.collection("galewela-bookings").doc("all").onSnapshot((doc) => {
    if (doc.exists && doc.data().bookings) {
      bookingState = doc.data().bookings;
      if (document.querySelector("#public-booking-calendar")) {
          renderPublicBookingCalendar();
      }
    }
  }, console.error);
}

