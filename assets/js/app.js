async function savePublicBooking(booking) {
  const realtimeDb = await getSiteRealtimeDb();
  if (realtimeDb) {
    await realtimeDb.ref(`galewela-bookings/${booking.id}`).set({
      ...booking,
      updatedAt: firebase.database.ServerValue.TIMESTAMP
    });
    bookingState = [booking, ...bookingState.filter((item) => item.id !== booking.id)];
    if (document.querySelector("#public-booking-calendar")) {
      renderPublicBookingCalendar();
    }
    return { ok: true };
  }
  const firestore = getSiteDb();
  if (!firestore) throw new Error("Firebase is not configured.");
  const ref = firestore.collection("galewela-bookings").doc("all");
  await firestore.runTransaction(async (transaction) => {
    const doc = await transaction.get(ref);
    const current = doc.exists && Array.isArray(doc.data().bookings) ? doc.data().bookings : [];
    transaction.set(ref, {
      bookings: [booking, ...current.filter((item) => item.id !== booking.id)],
      updatedAt: firebase.firestore.FieldValue.serverTimestamp()
    }, { merge: true });
  });
  return { ok: true };
}

async function saveBookings(bookings) {
  bookingState = Array.isArray(bookings) ? bookings : [];
  const realtimeDb = await getSiteRealtimeDb();
  if (realtimeDb) {
    await realtimeDb.ref("galewela-bookings").set(bookingsArrayToMap(bookingState));
    return;
  }
  const firestore = getSiteDb();
  if (!firestore) throw new Error("Firebase is not configured.");
  await firestore.collection("galewela-bookings").doc("all").set({
    bookings: bookingState,
    updatedAt: firebase.firestore.FieldValue.serverTimestamp()
  }, { merge: true });
}

